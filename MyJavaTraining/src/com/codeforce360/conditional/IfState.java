package com.codeforce360.conditional;

public class IfState {

	public static void main(String[] args) {
		int mark=90;
		if (mark>70){
			System.out.println("Grade A");
		}		
		else if(mark>80) {
		
			System.out.println("Grade B");
		}
		else if (mark>90)
		{
			System.out.println("Grade C");
		}
		else {
		  
			System.out.println("Failed");
			
		}
	}
}
	
	


